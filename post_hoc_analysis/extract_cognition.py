@dataclass
class ModelSummary:
    """One row per (model configuration, campaign signature) for the paper."""
    strategist_model: str
    campaign_signature: str
    n_runs: int
    peak_psr_mean: float | None
    peak_psr_std: float | None
    peak_centered_mean: float | None
    peak_centered_std: float | None
    iter_final_psr_mean: float | None
    iter_final_psr_std: float | None
    iter_final_centered_mean: float | None
    iter_final_centered_std: float | None
    collapse_count_mean: float
    collapse_count_std: float | None
    recovery_count_mean: float
    recovery_count_std: float | None
    stability_score_mean: float | None
    stability_score_std: float | None
    mean_excisions_per_iter_across_runs: float
    proposal_type_mean_per_iter: dict
    run_summaries: list


def aggregate_across_runs(runs, campaign_signature: str = ""):
    if not runs:
        raise ValueError("aggregate_across_runs: empty run list")
    models = {r.strategist_model for r in runs}
    if len(models) > 1:
        raise ValueError(
            f"aggregate_across_runs: runs span multiple models: {models}")

    peak_psrs = [r.peak_psr for r in runs]
    peak_cents = [r.peak_centered for r in runs]
    final_psrs = [r.iter_final_psr for r in runs]
    final_cents = [r.iter_final_centered for r in runs]
    collapses = [r.collapse_count for r in runs]
    recoveries = [r.recovery_count for r in runs]
    stabilities = [r.stability_score for r in runs]

    prop_type_means = {}
    for t in PROP_TYPES:
        prop_type_means[t] = _mean([r.mean_proposals_by_type.get(t, 0)
                                    for r in runs])

    return ModelSummary(
        strategist_model=models.pop(),
        campaign_signature=campaign_signature,
        n_runs=len(runs),
        peak_psr_mean=_mean(peak_psrs),
        peak_psr_std=_safe_std(peak_psrs),
        peak_centered_mean=_mean(peak_cents),
        peak_centered_std=_safe_std(peak_cents),
        iter_final_psr_mean=_mean(final_psrs),
        iter_final_psr_std=_safe_std(final_psrs),
        iter_final_centered_mean=_mean(final_cents),
        iter_final_centered_std=_safe_std(final_cents),
        collapse_count_mean=_mean(collapses) or 0.0,
        collapse_count_std=_safe_std(collapses),
        recovery_count_mean=_mean(recoveries) or 0.0,
        recovery_count_std=_safe_std(recoveries),
        stability_score_mean=_mean(stabilities),
        stability_score_std=_safe_std(stabilities),
        mean_excisions_per_iter_across_runs=_mean(
            [r.mean_excisions_per_iter for r in runs]) or 0.0,
        proposal_type_mean_per_iter=prop_type_means,
        run_summaries=runs,
    )


