    cross_seed_cv: float | None
    cross_seed_reward_std: float | None
    mean_final_reward: float | None
    critic_saturation_index: float | None
    trajectory_isomorphism_rho: float | None
    is_initialization_sensitive: bool | None
    is_universally_converged: bool | None
    within_seed_terminal_cv: float | None
    is_terminal_unstable: bool | None
    critic_diverged: bool | None
    optimization_unstable: bool | None

    # --- Reward Topology ---
    objective_alignment_rho: float | None
    survival_hacking_rho: float | None
    survival_hacking_detected: bool | None
    topology_inverted: bool | None
    intra_rollout_cv: float | None
    terminal_entropy_norm: float | None
    systemic_policy_collapse: bool | None

    # --- Kinematic / Evaluation ---
    population_success_rate: float | None
    cross_seed_success_std: float | None
    is_lottery_ticket: bool | None
    mean_descent_efficiency: float | None
    efficiency_cv: float | None
    actuator_chatter_rate: float | None
    chatter_cv: float | None
    kinematically_sensitive: bool | None
    macro_oscillations: float | None
    lateral_instability: bool | None

    # --- Population Terminal Distribution (from evaluation rollouts) ---
    terminal_distribution: dict
    landed_centered_rate: float | None
    landed_off_centered_rate: float | None
    landed_off_centered_timeout_rate: float | None
    landed_slid_rate: float | None
    crashed_rate: float | None
