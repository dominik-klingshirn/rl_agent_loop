"""
ARD post-hoc evaluation: extraction library.

Architecture:
    Per iteration we read FOUR structured sources and stitch them together:

      1. iter{NN}_metric_payload.json
         Deterministic RL outcome metrics. Generated by analysis.py from
         training/eval CSVs. Authoritative for: PSR, terminal distribution,
         ρ, SNR, CSI, per-component flags.

      2. iter{NN}_cognition_record.json
         The LLM call records. Authoritative for: proposal types, excisions,
         Research Lead selection. NOT used for outcome metrics — those
         metrics appear in the prompt text but represent the PRIOR
         iteration's diagnostic (the baseline-for-comparison section).

      3. experiment_ledger.json (single file at run root)
         The structured Validator verdicts. Authoritative for: status,
         post-mortem text.

       4. ChatResponse_data.csv  (joined to runs by row, not per-iter file)
        LLM compute data (durations, tokens) comes from a separate source

"""

from __future__ import annotations

import json
import re
from dataclasses import dataclass, field
from pathlib import Path
import ast as _ast  # noqa: F401 


# ===========================================================================
# Path resolution
# ===========================================================================

class RunPaths:
    """
    Resolves every per-iteration file path under one campaign folder.
    Mirrors workspace_manager.py's layout exactly.
    """

    def __init__(self, campaign_path: Path):
        self.campaign_path = Path(campaign_path)
        model_dirs = [p for p in self.campaign_path.iterdir() if p.is_dir()]
        if len(model_dirs) != 1:
            raise ValueError(
                f"Expected exactly one model directory under {campaign_path}, "
                f"found {len(model_dirs)}: {[d.name for d in model_dirs]}")
        self.model_dir = model_dirs[0]
        self.cognition_json_dir = (self.model_dir
                                   / "cognition" / "json_cognition_records")
        self.metric_payload_dir = (self.model_dir
                                   / "telemetry" / "metric_payloads")
        self.ledger_path = self.model_dir / "cognition" / "experiment_ledger.json"
        self.chat_response_path = (self.model_dir
                                   / "cognition" / "ChatResponse_data.csv")
        self.code_dir = self.model_dir / "generated_code" 

    @property
    def campaign_tag(self) -> str:
        return self.campaign_path.name

    @property
    def model_dir_name(self) -> str:
        return self.model_dir.name

    def cognition_record(self, iteration: int) -> Path:
        return self.cognition_json_dir / f"iter{iteration:02d}_cognition_record.json"

    def metric_payload(self, iteration: int) -> Path:
        return self.metric_payload_dir / f"iter{iteration:02d}_metric_payload.json"

    def discover_iterations(self) -> list[int]:
        """Iteration numbers for which BOTH a payload and cognition record exist."""
        cog = set()
        for p in self.cognition_json_dir.glob("iter*_cognition_record.json"):
            m = re.match(r"iter(\d+)_", p.name)
            if m:
                cog.add(int(m.group(1)))
        pay = set()
        if self.metric_payload_dir.is_dir():
            for p in self.metric_payload_dir.glob("iter*_metric_payload.json"):
                m = re.match(r"iter(\d+)_", p.name)
                if m:
                    pay.add(int(m.group(1)))
        return sorted(cog & pay)
    
    def patch_file(self, iteration: int):          # → Path
        """Unified diff saved by standard.py each iteration."""
        return self.code_dir / f"iter{iteration:02d}_changes.patch"

    def reward_file(self, iteration: int):         # → Path
        """Accepted reward.py saved by standard.py each iteration."""
        return self.code_dir / f"iter{iteration:02d}_reward.py"



