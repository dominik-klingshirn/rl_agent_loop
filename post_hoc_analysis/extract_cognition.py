def load_chat_responses(csv_path, cognition_json_dir=None):
    """
    Load ChatResponse_data.csv into a list of typed dicts.

    If cognition_json_dir is provided, response_content and thinking_trace
    are joined in from iter{NN}_cognition_record.json for the output size
    breakdown in the compute cost dashboard.

    Nanosecond durations are converted to seconds (stored alongside the
    originals under _s suffix). Numeric columns are coerced. The CSV
    already has one row per LLM call, so this is just type-cleaning.
    """
    import csv as _csv
    csv_path = Path(csv_path)
    if not csv_path.is_file():
        return []
    rows = []
    NS_COLS = ("total_duration_ns", "load_duration_ns",
               "prompt_eval_duration_ns", "eval_duration_ns")
    INT_COLS = ("iteration", "num_tool_calls",
                "prompt_eval_count", "eval_count")
    FLOAT_COLS = ("opt_temperature", "opt_top_p", "opt_top_k",
                  "opt_repeat_penalty", "opt_num_ctx", "opt_num_predict")
    with csv_path.open() as f:
        for r in _csv.DictReader(f):
            for c in NS_COLS:
                ns = r.get(c, "")
                if ns:
                    try:
                        ns_v = int(ns)
                        r[c] = ns_v
                        r[c.replace("_ns", "_s")] = ns_v / 1e9
                    except ValueError:
                        r[c] = None
            for c in INT_COLS:
                v = r.get(c, "")
                if v:
                    try:
                        r[c] = int(v)
                    except ValueError:
                        r[c] = None
            for c in FLOAT_COLS:
                v = r.get(c, "")
                if v:
                    try:
                        r[c] = float(v)
                    except ValueError:
                        r[c] = None
            # Ensure these keys always exist; populated by join below if available
            r.setdefault("response_content", "")
            r.setdefault("thinking_trace", "")
            rows.append(r)

    # Join response_content + thinking_trace from cognition JSON records.
    # Builds a lookup: {(iteration, phase) -> (response_content, thinking_trace)}
    if cognition_json_dir is not None:
        cog_dir = Path(cognition_json_dir)
        _lookup: dict[tuple[int, str], tuple[str, str]] = {}
        for json_path in sorted(cog_dir.glob("iter*_cognition_record.json")):
            try:
                with json_path.open() as jf:
                    data = json.load(jf)
                it_num = data.get("iteration")
                for call in data.get("calls", []):
                    phase = call.get("phase", "")
                    content = call.get("response_content") or ""
                    thinking = call.get("thinking_trace") or ""
                    if it_num is not None and phase:
                        _lookup[(it_num, phase)] = (content, thinking)
            except Exception:
                continue
        for r in rows:
            it = r.get("iteration")
            phase = r.get("phase", "")
            if it is not None:
                content, thinking = _lookup.get((it, phase), ("", ""))
                r["response_content"] = content
                r["thinking_trace"] = thinking

    return rows


