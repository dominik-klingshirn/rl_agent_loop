@dataclass
class OutcomeMetrics:
    """
    Deterministic outcome metrics for one iteration, loaded from the
    metric payload JSON. No regex, no markdown parsing.

    Landing rates (population_success_rate, all terminal_*_rate fields) are
    fractions 0-1, matching the payload's units.
    """
