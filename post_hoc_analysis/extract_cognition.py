@dataclass
class IterationRecord:
    """Everything we know about one iteration of one run."""
    iteration: int
    campaign_tag: str
    model_dir: str
    strategist_model: str
    role_models: dict
    outcomes: OutcomeMetrics
    cognition: CognitionRecord
    code_change: "CodeChangeRecord | None"
    validator_status: str | None
    ledger_hypothesis: str
    ledger_post_mortem: str | None
    parse_warnings: list = field(default_factory=list)


def assemble_iteration(paths: RunPaths, iteration: int,
                       ledger: dict[int, LedgerEntry]) -> IterationRecord:
    """Combine payload + cognition record + ledger into one IterationRecord."""
    warnings: list[str] = []

    payload_path = paths.metric_payload(iteration)
    cognition_path = paths.cognition_record(iteration)
    if not payload_path.is_file():
        raise FileNotFoundError(f"Missing payload: {payload_path}")
    if not cognition_path.is_file():
        raise FileNotFoundError(f"Missing cognition record: {cognition_path}")

    outcomes = load_metric_payload(payload_path)
    cognition = extract_cognition_record(cognition_path)
    warnings.extend(cognition.parse_warnings)
    try:
        code_change = load_code_change(paths, iteration)
        # Suppress the expected iter-1 baseline warning from top-level parse_warnings
        # to keep it out of the Pipeline Performance dashboard's warning counts.
        for w in code_change.warnings:
            if not w.startswith("iter1_no_prev"):
                warnings.append(f"code:{w}")
    except Exception as exc:
        code_change = None
        warnings.append(f"code_change_load_error:{exc}")

    ledger_entry = ledger.get(iteration)
    if ledger_entry is None:
        warnings.append("missing_ledger_entry")
        validator_status = None
        hypothesis       = ""
        post_mortem      = None
    else:
        validator_status = ledger_entry.status
        hypothesis       = ledger_entry.hypothesis_text
        post_mortem      = ledger_entry.post_mortem_text
        # Final iteration of a run will typically have post_mortem=None
        # because the Validator writes it at the start of iter N+1, which
        # never happens. Flag it so the dashboard can render distinctly.
        if post_mortem is None:
            warnings.append("ledger_post_mortem_pending")

    return IterationRecord(
        iteration=iteration,
        campaign_tag=paths.campaign_tag,
        model_dir=paths.model_dir_name,
        strategist_model=cognition.strategist_model,
        role_models=cognition.role_models,
        outcomes=outcomes,
        cognition=cognition,
        code_change=code_change,
        validator_status=validator_status,
        ledger_hypothesis=hypothesis,
        ledger_post_mortem=post_mortem,
        parse_warnings=warnings,
    )


