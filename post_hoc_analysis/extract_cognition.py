# Ledger loader (Validator verdicts)
# ===========================================================================

# Status vocabulary observed in the ledger. Pyrrhic Victory / Productive
# Deviation / Goodhart Trap are richer-than-original-spec verdicts the
# Validator has adopted in practice, so we preserve them rather than
# collapsing to the original five-category vocabulary.
LEDGER_STATUS_VOCAB = (
    "Validated", "Confirmed",
    "Mixed", "Pyrrhic Victory", "Productive Deviation",
    "Inconclusive",
    "Refuted", "Regressed", "Goodhart Trap",
    "Unparsed",
)


@dataclass
class LedgerEntry:
    iteration: int
    hypothesis_text: str
    post_mortem_text: str | None
    status: str | None


def _extract_ledger_status(post_mortem: str | None) -> str | None:
    """Extract the verdict from `Status:` in the post-mortem; longest-match vs vocab."""
    if not post_mortem:
        return None
    m = re.search(r"\*\*Status:?\*\*\s*[`'\"]?\s*([A-Za-z][A-Za-z\- ]*)",
                  post_mortem)
    if not m:
        return None
    raw = m.group(1).strip().strip("`'\"").rstrip(" -").strip()
    raw_lower = raw.lower()
    best = None
    for v in LEDGER_STATUS_VOCAB:
        if v == "Unparsed":
            continue
        if raw_lower.startswith(v.lower()):
            if best is None or len(v) > len(best):
                best = v
    if best:
        return best
    return raw.split()[0] if raw else None


def load_ledger(ledger_path: Path) -> dict[int, LedgerEntry]:
    """Load the experiment ledger; return {iteration: LedgerEntry}."""
    if not ledger_path.is_file():
        return {}
    with ledger_path.open() as f:
        d = json.load(f)
    out = {}
    for exp in d.get("experiments", []):
        it = exp["id"]
        out[it] = LedgerEntry(
            iteration=it,
            hypothesis_text=exp.get("hypothesis_payload", "") or "",
            post_mortem_text=exp.get("validation_post_mortem"),
            status=_extract_ledger_status(exp.get("validation_post_mortem")),
        )
    return out


