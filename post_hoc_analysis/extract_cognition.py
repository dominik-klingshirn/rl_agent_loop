PROP_TYPES = ("modification", "addition", "cluster", "unknown")


@dataclass
class ProposalRecord:
    index: int
    header: str
    inferred_type: str
    type_evidence: str


def _infer_proposal_type(body: str, header: str) -> tuple[str, str]:
    """Classify a Strategist proposal as Modification / Addition / Cluster."""
    h = header.lower()
    body_l = body.lower()

    paren = re.search(r"\(([^)]+)\)\s*$", header)
    if paren:
        ptxt = paren.group(1).lower()
        if "cluster" in ptxt or "synergistic" in ptxt:
            return "cluster", f"header:{paren.group(1)}"
        if "modification" in ptxt or "modify" in ptxt:
            return "modification", f"header:{paren.group(1)}"
        if "addition" in ptxt or "add" in ptxt:
            return "addition", f"header:{paren.group(1)}"

    # Cluster signals first — "replace X and Y with a single unified term"
    # describes a cluster, not a one-component rescale. Cheap two-step check
    # avoids catastrophic backtracking from nested-quantifier regex.
    if re.search(r"\bsynergistic\s+cluster\b|\bcluster\b.*\bsynerg", body_l):
        return "cluster", "body:synergistic-cluster"
    UNIFY_TARGETS = ("a single", "single unified", "synergistic term",
                     "unified term", "combined term", "into a single",
                     "merged into")
    if (re.search(r"\b(?:replac|modif|merg|combin|unif)[a-z]*\b", body_l)
            and any(t in body_l for t in UNIFY_TARGETS)):
        return "cluster", "body:multi-into-single"
    if re.search(r"\bcluster\b", h):
        return "cluster", "header:cluster"

    if re.search(r"\breplac(?:e|ing)\b\s+(?:the\s+)?[`\w_]+", body_l):
        return "modification", "body:replace"
    if "**replace**" in body_l:
        return "modification", "body:replace-bold"
    if re.search(r"\*\*modify\*\*|\bmodify\s+(?:the\s+)?[`\w]+", body_l):
        return "modification", "body:modify"
    if "old form" in body_l and "new form" in body_l:
        return "modification", "body:old-new-form"
    if re.search(r"\b(?:invert|inversion|sign\s+inversion|rescale|gating?|gate\s+by)\b",
                 body_l):
        return "modification", "body:invert-rescale-gate"
    if re.search(r"\*\*add\*\*|\badd\s+a\s+new\s+component\b|\bnew\s+term\s*[:\.]",
                 body_l):
        return "addition", "body:add-new"
    # "Add the following term", "Add a term", "Add this term"
    if re.search(r"\badd\s+(?:the\s+following|a|this|new)\s+(?:term|component|reward|bonus|penalty)\b",
                 body_l):
        return "addition", "body:add-following"
    if re.search(r"\bpropose\s+(?:adding|introducing)\b|\bintroduc(?:e|ing)\s+a\s+new\b",
                 body_l):
        return "addition", "body:propose-add"

    return "unknown", ""


def _parse_strategist_excisions(response: str) -> int | None:
    """Count components excised in PART 1 of the Strategist response."""
    part1 = re.search(r"PART 1[^\n]*\n(.*?)(?:PART 2|\Z|\*\*PART 2)",
                      response, re.DOTALL | re.IGNORECASE)
    if not part1:
        return None
    text = part1.group(1)
    if re.search(r"no components?\s+(?:are\s+)?(?:being\s+)?excised", text, re.I):
        return 0
    return len(re.findall(r"^\s*[\*\-]\s+\*\*", text, re.MULTILINE))


def _parse_strategist_proposals(response: str) -> list[ProposalRecord]:
    matches = list(re.finditer(r"^###\s*Proposal\s*(\d+)[^\n]*",
                               response, re.MULTILINE))
    if not matches:
        return []
    proposals = []
    for i, m in enumerate(matches):
        header = m.group(0).lstrip("# ").strip()
        idx = int(m.group(1))
        body_start = m.end()
        body_end = (matches[i + 1].start()
                    if i + 1 < len(matches) else len(response))
        body = response[body_start:body_end]
        ptype, evidence = _infer_proposal_type(body, header)
        proposals.append(ProposalRecord(
            index=idx, header=header,
            inferred_type=ptype, type_evidence=evidence))
    return proposals


def _parse_rl_selection(response: str) -> tuple[int | None, str | None]:
    m = re.search(r"\*\*Selected Proposal:?\*\*\s*Proposal\s*(\d+)\s*:?\s*([^\n]*)",
                  response)
    if not m:
        m = re.search(r"EXECUTIVE DECISION.*?Proposal\s*(\d+)\s*:?\s*([^\n]*)",
                      response, re.DOTALL | re.IGNORECASE)
        if not m:
            return None, None
    return int(m.group(1)), m.group(2).strip()


@dataclass
class CognitionRecord:
    """LLM behaviour for one iteration (no outcome metrics here)."""
    iteration: int
    strategist_model: str
    role_models: dict
    excision_count: int | None
    proposal_type_counts: dict
    proposals: list
    selected_proposal_index: int | None
    selected_proposal_header: str | None
    parse_warnings: list = field(default_factory=list)

@dataclass
class CodeChangeRecord:
    """
    Ground-truth code-level changes for one iteration.

    Sources
    -------
    Diff stats (lines_added / lines_removed / n_hunks):
        Parsed from iter{N}_changes.patch.  Raw signal only — no attempt to
        track the components block through diff prefixes.

    Component delta (excised_components / added_components):
        AST set-difference between iter{N-1}_reward.py and iter{N}_reward.py.
        This is immune to the Strategist's self-reported PART 1 prose, which
        can silently undercount (e.g. iter06: "no excisions" while
        landing_stability_synergy was actually removed from the dict).

    Structural integrity (double_count_flags / ghost_vars):
        AST analysis of iter{N}_reward.py only.
        double_count_flags: component pairs whose expanded r_* variable sets
            overlap, meaning the same intermediate quantity appears in total_reward
            more than once.
        ghost_vars: r_* variables assigned in the function body but not reachable
            (directly or transitively through other r_* assignments) from any
            component dict value.  Benign aggregation aliases (r_landing etc.)
            appear here; treat as low-severity unless the name matches a key that
            was expected to be tracked.
    """
    iteration: int
    # Raw diff statistics
    lines_added: int
    lines_removed: int
    n_hunks: int
    patch_available: bool
    # Component-level delta (AST comparison: curr vs prev reward.py)
    excised_components: list   # component keys present in prev but absent in curr
    added_components: list     # component keys absent in prev but present in curr
    n_excised: int
    n_added: int
    # Current-iteration structural integrity (AST of curr reward.py)
    component_names: list      # ordered keys in components dict this iteration
    n_components: int
    double_count_flags: list   # [{component_a, component_b, shared_r_vars}]
    ghost_vars: list           # r_* vars computed but not reachable from components
    ast_available: bool
    warnings: list


def extract_cognition_record(record_path: Path) -> CognitionRecord:
    """Parse iter{NN}_cognition_record.json — LLM behaviour fields only."""
    with record_path.open() as f:
        data = json.load(f)

    iteration = data["iteration"]
    strategist_model = data.get("model_name", "")
    calls_by_phase = {c["phase"]: c for c in data["calls"]}
    role_models = {phase: c.get("model_name", "")
                   for phase, c in calls_by_phase.items()}
    warnings: list[str] = []

    strat_call = calls_by_phase.get("strategist")
    if strat_call is None:
        warnings.append("missing_strategist_call")
        excision_count = None
        proposals = []
    else:
        resp = strat_call["response_content"] or ""
        excision_count = _parse_strategist_excisions(resp)
        proposals = _parse_strategist_proposals(resp)
        if not proposals:
            warnings.append("no_proposals_parsed")
        for p in proposals:
            if p.inferred_type == "unknown":
                warnings.append(f"proposal_{p.index}_type_unknown")

    proposal_type_counts = {t: 0 for t in PROP_TYPES}
    for p in proposals:
        proposal_type_counts[p.inferred_type] = \
            proposal_type_counts.get(p.inferred_type, 0) + 1

    rl_call = calls_by_phase.get("research_lead")
    if rl_call is None:
        warnings.append("missing_research_lead_call")
        selected_idx, selected_header = None, None
    else:
        selected_idx, selected_header = _parse_rl_selection(
            rl_call["response_content"] or "")
        if selected_idx is None:
            warnings.append("selection_unparsed")

    return CognitionRecord(
        iteration=iteration,
        strategist_model=strategist_model,
        role_models=role_models,
        excision_count=excision_count,
        proposal_type_counts=proposal_type_counts,
        proposals=proposals,
        selected_proposal_index=selected_idx,
        selected_proposal_header=selected_header,
        parse_warnings=warnings,
    )

def _collect_r_names(node) -> set:
    """Collect all Name.id values matching r_* from an AST subtree."""
    return {
        n.id for n in _ast.walk(node)
        if isinstance(n, _ast.Name) and n.id.startswith("r_")
    }


def analyze_reward_ast(source: str) -> dict:
    """
    Parse reward.py source and return structural analysis.

    Double-count detection
    ~~~~~~~~~~~~~~~~~~~~~~
    For each component, collect the r_* variables its value expression
    references directly, then expand one level through the function's
    r_* assignment graph.  Two components are flagged as a double-count
    pair when their expanded sets overlap — meaning the same intermediate
    quantity is summed into total_reward more than once.

    Example (iter05):
        action_smoothness         = float(r_smooth)   → expanded: {r_smooth}
        lateral_damping           = float(r_lateral)  → expanded: {r_lateral}
        landing_stability_synergy = float(r_lss)
            where r_lss = r_smooth + r_lateral        → expanded: {r_lss, r_smooth, r_lateral}
        → pairs flagged: (synergy ↔ action_smoothness), (synergy ↔ lateral_damping)

    Ghost variable detection
    ~~~~~~~~~~~~~~~~~~~~~~~~
    Any r_* variable assigned in the function body but not in the union of
    all expanded component variable sets is a ghost.  Aggregation aliases
    (r_landing = r_a + r_b + r_c, where a/b/c are individually tracked)
    always appear here; flag them but treat as informational.

    Returns a dict; parse_error is None on success.
    """
    _EMPTY = {
        "component_names": [], "n_components": 0,
        "double_count_flags": [], "ghost_vars": [],
        "parse_error": None,
    }
    try:
        tree = _ast.parse(source)
    except SyntaxError as exc:
        return {**_EMPTY, "parse_error": str(exc)}

    func = next(
        (n for n in _ast.walk(tree)
         if isinstance(n, _ast.FunctionDef) and n.name == "calculate_reward"),
        None,
    )
    if func is None:
        return {**_EMPTY, "parse_error": "calculate_reward not found"}

    # Build r_* variable dependency map: var → set of r_* vars its RHS references
    r_var_deps: dict = {}
    for node in _ast.walk(func):
        if isinstance(node, _ast.Assign):
            for target in node.targets:
                if isinstance(target, _ast.Name) and target.id.startswith("r_"):
                    r_var_deps[target.id] = _collect_r_names(node.value)

    # Locate the `components = { ... }` assignment
    comp_dict = None
    for node in _ast.walk(func):
        if (isinstance(node, _ast.Assign)
                and any(isinstance(t, _ast.Name) and t.id == "components"
                        for t in node.targets)
                and isinstance(node.value, _ast.Dict)):
            comp_dict = node.value
            break
    if comp_dict is None:
        return {**_EMPTY, "parse_error": "components dict not found"}

    # Extract component_name → directly referenced r_* vars
    component_names: list = []
    comp_r_refs: dict = {}
    for key_node, val_node in zip(comp_dict.keys, comp_dict.values):
        if not (isinstance(key_node, _ast.Constant)
                and isinstance(key_node.value, str)):
            continue
        comp_name = key_node.value
        component_names.append(comp_name)
        # Unwrap float(expr) if present so we inspect the actual expression
        inner = (val_node.args[0]
                 if isinstance(val_node, _ast.Call) and val_node.args
                 else val_node)
        comp_r_refs[comp_name] = _collect_r_names(inner)

    def _expand(r_vars: set) -> set:
        """One-level expansion through r_* assignment graph."""
        expanded = set(r_vars)
        for v in r_vars:
            expanded |= r_var_deps.get(v, set())
        return expanded

    # Double-count: flag all pairs (A, B) with overlapping expanded r_* sets
    double_count_flags: list = []
    comp_items = list(comp_r_refs.items())
    for i, (comp_a, refs_a) in enumerate(comp_items):
        exp_a = _expand(refs_a)
        for comp_b, refs_b in comp_items[i + 1:]:
            exp_b = _expand(refs_b)
            overlap = exp_a & exp_b
            if overlap:
                double_count_flags.append({
                    "component_a": comp_a,
                    "component_b": comp_b,
                    "shared_r_vars": sorted(overlap),
                })

    # Ghost detection: r_* vars not reachable from any component value
    all_comp_r_vars: set = set()
    for refs in comp_r_refs.values():
        all_comp_r_vars |= _expand(refs)
    ghost_vars = sorted(v for v in r_var_deps if v not in all_comp_r_vars)

    return {
        "component_names": component_names,
        "n_components": len(component_names),
        "double_count_flags": double_count_flags,
        "ghost_vars": ghost_vars,
        "parse_error": None,
    }


def parse_reward_diff(patch_text: str) -> dict:
    """
    Extract raw diff statistics from a unified diff of reward.py.

    Deliberately minimal: only lines_added, lines_removed, n_hunks.
    Component-level excision/addition is derived via AST comparison in
    load_code_change, not from diff text — tracking brace depth through
    diff prefixes is fragile when hunks split the components block.
    """
    lines_added = lines_removed = n_hunks = 0
    for line in patch_text.splitlines():
        if line.startswith("@@"):
            n_hunks += 1
        elif line.startswith("+") and not line.startswith("+++"):
            lines_added += 1
        elif line.startswith("-") and not line.startswith("---"):
            lines_removed += 1
    return {
        "lines_added": lines_added,
        "lines_removed": lines_removed,
        "n_hunks": n_hunks,
    }


def load_code_change(paths, iteration: int) -> CodeChangeRecord:
    """
    Load and derive all code-change metrics for one iteration.

    Patch file  → raw diff statistics.
    curr reward.py AST → component_names, double_count_flags, ghost_vars.
    prev reward.py AST → component_names for set-difference (excised/added).

    For iteration 1 there is no iter00_reward.py; all current components
    are treated as "added" and a non-critical warning is emitted.
    """
    warnings: list = []

    # ── Patch: raw diff statistics ──
    patch_path = paths.patch_file(iteration)
    patch_available = patch_path.is_file()
    lines_added = lines_removed = n_hunks = 0
    if patch_available:
        try:
            stats = parse_reward_diff(patch_path.read_text(encoding="utf-8"))
            lines_added   = stats["lines_added"]
            lines_removed = stats["lines_removed"]
            n_hunks       = stats["n_hunks"]
        except Exception as exc:
            warnings.append(f"patch_parse_error:{exc}")
            patch_available = False
    else:
        warnings.append("patch_missing")

    # ── Current reward.py: structural analysis ──
    curr_path = paths.reward_file(iteration)
    ast_available = curr_path.is_file()
    curr_analysis: dict = {}
    if ast_available:
        try:
            curr_analysis = analyze_reward_ast(curr_path.read_text(encoding="utf-8"))
            if curr_analysis.get("parse_error"):
                warnings.append(f"ast_error:{curr_analysis['parse_error']}")
                ast_available = False
        except Exception as exc:
            warnings.append(f"ast_error:{exc}")
            ast_available = False
    else:
        warnings.append("reward_py_missing")

    curr_components = curr_analysis.get("component_names", [])

    # ── Previous reward.py: component names for set-difference ──
    prev_path = paths.reward_file(iteration - 1)
    prev_components: list = []
    if prev_path.is_file():
        try:
            prev_analysis = analyze_reward_ast(prev_path.read_text(encoding="utf-8"))
            if not prev_analysis.get("parse_error"):
                prev_components = prev_analysis.get("component_names", [])
            else:
                warnings.append(f"prev_ast_error:{prev_analysis['parse_error']}")
        except Exception as exc:
            warnings.append(f"prev_ast_error:{exc}")
    elif iteration == 1:
        warnings.append("iter1_no_prev_baseline")
    else:
        warnings.append(f"prev_reward_py_missing:iter{iteration - 1:02d}")

    curr_set = set(curr_components)
    prev_set = set(prev_components)
    excised = sorted(prev_set - curr_set)
    added   = sorted(curr_set - prev_set)

    return CodeChangeRecord(
        iteration=iteration,
        lines_added=lines_added,
        lines_removed=lines_removed,
        n_hunks=n_hunks,
        patch_available=patch_available,
        excised_components=excised,
        added_components=added,
        n_excised=len(excised),
        n_added=len(added),
        component_names=curr_components,
        n_components=len(curr_components),
        double_count_flags=curr_analysis.get("double_count_flags", []),
        ghost_vars=curr_analysis.get("ghost_vars", []),
        ast_available=ast_available,
        warnings=warnings,
    )


