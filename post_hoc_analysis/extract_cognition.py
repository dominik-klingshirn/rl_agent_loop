def _safe_max(values):
    vs = [v for v in values if v is not None]
    return max(vs) if vs else None


def _safe_std(values):
    vs = [v for v in values if v is not None]
    if len(vs) < 2:
        return None
    mean = sum(vs) / len(vs)
    return (sum((v - mean) ** 2 for v in vs) / len(vs)) ** 0.5


def _mean(values):
    vs = [v for v in values if v is not None]
    return (sum(vs) / len(vs)) if vs else None


def _count_collapses_recoveries(rates, collapse_threshold=0.10, recovery_threshold=0.50,
                                recovery_window=0):
    """Walk landing-rate sequence; count collapse/recovery events. (Unused — kept for reference.)"""
    collapses = 0
    recoveries = 0
    last_collapse_idx = None
    for i, r in enumerate(rates):
        if r is None:
            continue
        if r < collapse_threshold:
            collapses += 1
            last_collapse_idx = i
        elif r >= recovery_threshold and last_collapse_idx is not None:
            in_window = (recovery_window == 0
                         or (i - last_collapse_idx) <= recovery_window)
            if in_window:
                recoveries += 1
                last_collapse_idx = None
    return collapses, recoveries


def aggregate_run(iterations, cfg=None):
    cfg = cfg or AggregationConfig()
    if not iterations:
        raise ValueError("aggregate_run: empty iteration list")
    iterations = list(iterations)

    psrs = [it.outcomes.population_success_rate for it in iterations]
    cents = [it.outcomes.landed_centered_rate for it in iterations]

    total_excisions = sum(it.cognition.excision_count or 0 for it in iterations)
    mean_excisions = total_excisions / len(iterations)

    total_props = {t: 0 for t in PROP_TYPES}
    for it in iterations:
        for t, c in it.cognition.proposal_type_counts.items():
            total_props[t] = total_props.get(t, 0) + c
    mean_props = {t: total_props[t] / len(iterations) for t in total_props}

    verdicts = {}
    for it in iterations:
        v = it.validator_status or "Unparsed"
        verdicts[v] = verdicts.get(v, 0) + 1

    return RunSummary(
        campaign_tag=iterations[0].campaign_tag,
        model_dir=iterations[0].model_dir,
        strategist_model=iterations[0].strategist_model,
        iteration_count=len(iterations),
        peak_psr=_safe_max(psrs),
        peak_centered=_safe_max(cents),
        iter_final_psr=psrs[-1],
        iter_final_centered=cents[-1],
        collapse_count=0,
        recovery_count=0,
        stability_score=_safe_std(psrs),
        total_excisions=total_excisions,
        mean_excisions_per_iter=mean_excisions,
        total_proposals_by_type=total_props,
        mean_proposals_by_type=mean_props,
        validator_verdicts=verdicts,
        iterations=iterations,
        iterations_with_warnings=sum(1 for it in iterations if it.parse_warnings),
    )


def load_run(campaign_path, cfg=None):
    """Load every iteration in a campaign folder; return RunSummary."""
    paths = RunPaths(Path(campaign_path))
    ledger = load_ledger(paths.ledger_path)
    iters = paths.discover_iterations()
    if not iters:
        raise FileNotFoundError(f"No iterations found under {campaign_path}")
    records = [assemble_iteration(paths, it, ledger) for it in iters]
    return aggregate_run(records, cfg)


